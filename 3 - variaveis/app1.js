// variáveis em Javascript => São maneiras de armazenarmos informações
// para usarmos posteriormente

// Tipo de variável, nome da variável, valor

// VAR => Maneira natiga de variável dentro do Javascript
// permite que adicionemos um valor nela e posteriormente  possamos alterar esse valor

var nome = "Paulo Nolleto";
var nome = "Milenna Nolleto";
console.log(`Meu nome é: ${nome}`);

//LET => Adicionar um valor nele e também podemos alterar esse valor
let nomeLet = "Milenna";

nomeLet = "Mirella";
console.log(`Meu nome é: ${nomeLet}`);

//CONST => Adicionamos um valor e não podemos mais substituir esse valor
const nomeConst = "James Bond";
console.log(`Meu nome é: ${nomeConst}`);

let sobrenome = "Nolleto";
console.log(`Meu sobrenome é: ${sobrenome}`);
